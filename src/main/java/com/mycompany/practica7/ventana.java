    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Pelicula pel = new Pelicula(jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), jTextField4.getText());
        hashPel.add(pel);
        treePel.add(jTextField1.getText());
        arrPel.add(pel);
        llPel.add(pel);
        int x = mapPel.size();
        mapPel.put(x + 1, pel);
        System.out.println("\nHashSet:");
        for (Pelicula i : hashPel) {
            System.out.println(i.getNombre());
        }
        System.out.println("\nTreeSet:");
        for (String i : treePel) {
            System.out.println(i);
        }
        System.out.println("\nArrayList:");
        for (int i = 0; i < arrPel.size(); i++) {
            System.out.println(arrPel.get(i).getNombre());
        }
        System.out.println("\nLinkedList:");
        for (int i = 0; i < llPel.size(); i++) {
            System.out.println(llPel.get(i).getNombre());
        }
        System.out.println("\nLinkedHashMap:");
        for (int i = 1; i <= mapPel.size(); i++) {
            System.out.println(mapPel.get(i).getNombre());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

